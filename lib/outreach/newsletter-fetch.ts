/**
 * Newsletter fetch + clean for forward-mode outreach.
 *
 * When the outreach pipeline runs in `templateMode=forward`, we don't
 * compose a short "Hi X, noticed your firm" email — instead we grab
 * today's actual FundOps Daily newsletter from Danny's Gmail inbox
 * (where it lands as a normal delivery because Danny is himself a
 * subscriber), strip the recipient-specific + tracking bits, and
 * forward the cleaned body to each targeted recipient with a personal
 * note at the top.
 *
 * Why forward the newsletter vs. re-render from Supabase:
 *   - Authentic "Fwd: FundOps Daily — ..." vibe in the recipient's
 *     inbox. Reads like a real human forward.
 *   - We don't have to duplicate Resend's template rendering logic.
 *   - The recipient sees exactly what Danny's real subscribers see.
 */

import { listInboxMessages, getMessageContent } from './gmail-client'
import type { GmailMessageContent } from './gmail-client'

/**
 * Structured payload for a single-day newsletter, ready to forward.
 * `cleanedHtml` and `cleanedText` have had the recipient-specific
 * subscribe/unsubscribe block and tracking elements removed.
 */
export interface NewsletterPayload {
  sourceMessageId: string
  subject: string
  fromName: string
  fromEmail: string
  date: string // RFC 2822 date from the original message
  cleanedHtml: string
  cleanedText: string
  originalHtmlLength: number
  cleanedHtmlLength: number
}

const NEWSLETTER_FROM = 'feedback@fundopshq.com'
const NEWSLETTER_SUBJECT_PREFIX = 'FundOps Daily'

/**
 * Fetch the most recent FundOps Daily newsletter delivery from Danny's
 * Gmail inbox. Uses Gmail's search to find today's newsletter by sender
 * and subject prefix. Returns a cleaned payload ready to forward.
 */
export async function fetchTodaysNewsletter(): Promise<NewsletterPayload> {
  // Search query — find by sender + subject prefix, limited to the last
  // 24 hours so we don't accidentally pick up yesterday's when today's
  // hasn't landed yet. Excludes `[TEST]`-prefixed subjects so that
  // test previews sent via scripts/send-test-email.ts can't hijack
  // forward-mode outreach if they land in the inbox before the real
  // newsletter. If the query returns nothing, the cron wasn't able to
  // read Danny's morning newsletter and we fail the run.
  const query = `from:${NEWSLETTER_FROM} subject:"${NEWSLETTER_SUBJECT_PREFIX}" -subject:"[TEST]" newer_than:1d`
  const messages = await listInboxMessages({ query, maxResults: 5 })

  if (messages.length === 0) {
    throw new Error(
      `fetchTodaysNewsletter: no newsletter found in inbox matching "${query}"`,
    )
  }

  // Gmail returns messages newest-first — take the first one.
  const topId = messages[0].id
  const content = await getMessageContent(topId)

  if (!content.html) {
    throw new Error(
      `fetchTodaysNewsletter: newsletter ${topId} has no text/html part (text length=${content.text.length})`,
    )
  }

  const cleaned = cleanNewsletterContent(content)

  return {
    sourceMessageId: content.id,
    subject: content.subject,
    fromName: content.fromName,
    fromEmail: content.fromEmail,
    date: content.date,
    cleanedHtml: cleaned.html,
    cleanedText: cleaned.text,
    originalHtmlLength: content.html.length,
    cleanedHtmlLength: cleaned.html.length,
  }
}

/**
 * Strip recipient-specific tokens, tracking pixels, and the "you
 * subscribed" footer from the newsletter body. Both HTML and text are
 * cleaned so the multipart/alternative forward has matching content.
 *
 * What gets stripped:
 *   1. The entire unsubscribe footer block (contains Danny's own
 *      subscriber token — forwarding it unchanged would unsubscribe
 *      Danny from his own newsletter when the recipient clicks).
 *   2. The "You're receiving this because you subscribed" line.
 *   3. 1x1 tracking pixels and obvious open-tracking img tags.
 *
 * What's preserved:
 *   - All article content, headlines, summaries, source citations
 *   - The "Visit FundOpsHQ →" CTA links
 *   - The header + sponsorship block
 *   - Section headers ("The Wire", "Section A", etc.)
 *   - Inline styles, fonts, colors
 *
 * Design note: we do text-based find-and-replace rather than parsing
 * the HTML into a DOM. The newsletter is generated by a known template
 * (Resend with Danny's custom layout) so the patterns we're stripping
 * are stable. DOM parsing adds a heavy dependency (jsdom/cheerio) for
 * marginal benefit. If the Resend template changes in a way that
 * breaks the strip patterns, the debug route will catch it before
 * anything ships.
 */
export function cleanNewsletterContent(content: GmailMessageContent): {
  html: string
  text: string
} {
  return {
    html: cleanHtml(content.html),
    text: cleanText(content.text),
  }
}

function cleanHtml(html: string): string {
  let cleaned = html

  // 1. Unwrap Resend click-tracking redirect links BEFORE any other
  //    cleaning — otherwise downstream regexes won't recognize the real
  //    URLs (e.g., the unsubscribe URL is wrapped and URL-encoded, so
  //    a plain /api/newsletter/unsubscribe match would miss it).
  //
  //    Resend wraps every link as:
  //      https://<customer-hash>.us-east-1.resend-clicks-a.com/CL0/
  //        <url-encoded-target>/<count>/<run-id>/<verify-token>=<digits>
  //
  //    The target is URL-encoded (%2F for slash, %3F for ?) so the first
  //    raw `/` after `/CL0/` delimits the target from the path suffix.
  cleaned = cleaned.replace(
    /https?:\/\/[a-z0-9]+\.us-east-1\.resend-clicks-a\.com\/CL0\/([^/\s"'<>]+)\/\d+\/[a-f0-9-]+\/[^\s"'<>]+/gi,
    (_match, encodedTarget: string) => {
      try {
        return decodeURIComponent(encodedTarget)
      } catch {
        // If decode fails (bad percent-escape), return a safe homepage
        // rather than leaving the tracking wrapper in place.
        return 'https://fundopshq.com'
      }
    },
  )

  // 2. Remove the hidden Resend open-tracking pixel at the bottom of
  //    the message. Two orthogonal patterns so we catch variants:
  //    (a) inline style with width:1px / height:1px, and
  //    (b) attribute form width="1" / height="1".
  cleaned = cleaned.replace(
    /<img\b[^>]*style=["'][^"']*(?:width\s*:\s*1px|height\s*:\s*1px)[^"']*["'][^>]*>/gi,
    '',
  )
  cleaned = cleaned.replace(
    /<img\b[^>]*(?:width=["']?1["']?|height=["']?1["']?)[^>]*>/gi,
    '',
  )
  // (c) Any img whose src points at the Resend tracking domain —
  //     catches beacon pixels that don't declare 1px dimensions.
  cleaned = cleaned.replace(
    /<img\b[^>]*src=["']https?:\/\/[a-z0-9]+\.us-east-1\.resend-clicks-a\.com\/[^"']*["'][^>]*>/gi,
    '',
  )

  // 3. Remove the unsubscribe footer block. The newsletter ends with:
  //    "You're receiving this because you subscribed at fundopshq.com."
  //    "Unsubscribe · Visit FundOpsHQ · Live Show"
  //    Plus an unsubscribe link with a recipient-specific token (Danny's
  //    own token, since the pipeline fetches from his personal inbox).
  //
  //    The "You're" in the HTML is usually `You&rsquo;re` (HTML entity),
  //    not a Unicode apostrophe. Match all plausible forms.
  const footerMarkers = [
    /You(?:&rsquo;|&#8217;|&#x2019;|&apos;|['\u2019])re receiving this because/i,
    // After step 1, Resend-wrapped unsubscribe URLs have been decoded
    // back to plain /api/newsletter/unsubscribe?token=... so this matches.
    /\/api\/newsletter\/unsubscribe\?token=/i,
  ]
  let earliestFooterIdx = -1
  for (const re of footerMarkers) {
    const match = re.exec(cleaned)
    if (match && (earliestFooterIdx === -1 || match.index < earliestFooterIdx)) {
      earliestFooterIdx = match.index
    }
  }

  if (earliestFooterIdx !== -1) {
    // Walk backwards from the marker to find the nearest enclosing
    // table-cell container. Only `table`/`tr`/`td` — intentionally NOT
    // `<p>` or `<div>`, because the footer "You're receiving this..."
    // text lives inside a `<p>` that's a SIBLING of another `<p>` with
    // the "Unsubscribe / Visit / Live Show" links. Walking back to the
    // innermost `<p>` would only strip one of the two paragraphs.
    // Walking back to the enclosing `<td>` catches both.
    const before = cleaned.slice(0, earliestFooterIdx)
    const containerRe = /<(table|tr|td)\b[^>]*>/gi
    let lastOpen: { start: number; tag: string } | null = null
    let m
    while ((m = containerRe.exec(before)) !== null) {
      lastOpen = { start: m.index, tag: m[1].toLowerCase() }
    }

    if (lastOpen) {
      const tag = lastOpen.tag
      const openRe = new RegExp(`<${tag}\\b[^>]*>`, 'gi')
      const closeRe = new RegExp(`</${tag}>`, 'gi')
      let depth = 0
      let pos = lastOpen.start
      let closeEnd = -1
      while (pos < cleaned.length) {
        openRe.lastIndex = pos
        closeRe.lastIndex = pos
        const nextOpen = openRe.exec(cleaned)
        const nextClose = closeRe.exec(cleaned)
        if (!nextClose) break
        if (nextOpen && nextOpen.index < nextClose.index) {
          depth++
          pos = nextOpen.index + nextOpen[0].length
        } else {
          depth--
          pos = nextClose.index + nextClose[0].length
          if (depth === 0) {
            closeEnd = pos
            break
          }
        }
      }
      if (closeEnd !== -1) {
        cleaned =
          cleaned.slice(0, lastOpen.start) +
          '\n<!-- subscribe footer stripped for forward -->\n' +
          cleaned.slice(closeEnd)
      } else {
        cleaned =
          cleaned.slice(0, earliestFooterIdx) +
          '\n<!-- subscribe footer stripped (fallback: to EOF) -->\n'
      }
    } else {
      cleaned =
        cleaned.slice(0, earliestFooterIdx) +
        '\n<!-- subscribe footer stripped (fallback: to EOF) -->\n'
    }
  }

  // 4. Belt-and-suspenders: after all above, nuke any lingering
  //    unsubscribe-token URLs in case the block strip missed a variant.
  //    Matches both raw and URL-encoded forms.
  cleaned = cleaned.replace(
    /https?:\/\/[^"'\s<>]*\/api\/newsletter\/unsubscribe\?token=[^"'\s<>]*/gi,
    'https://fundopshq.com',
  )
  cleaned = cleaned.replace(
    /https?:\/\/[^"'\s<>]*%2Fapi%2Fnewsletter%2Funsubscribe[^"'\s<>]*/gi,
    'https://fundopshq.com',
  )

  return cleaned
}

function cleanText(text: string): string {
  let cleaned = text

  // Mirror the HTML strip at the text/plain level so the multipart
  // alternatives match. Find the footer marker and truncate.
  const footerMarker =
    /You['\u2019]re receiving this because you subscribed at fundopshq\.com/i
  const match = footerMarker.exec(cleaned)
  if (match) {
    cleaned = cleaned.slice(0, match.index).trimEnd() + '\n'
  }

  // Remove any stray unsubscribe token URLs.
  cleaned = cleaned.replace(
    /https?:\/\/[^\s]*\/api\/newsletter\/unsubscribe\?token=\S*/gi,
    'https://fundopshq.com',
  )

  return cleaned
}
